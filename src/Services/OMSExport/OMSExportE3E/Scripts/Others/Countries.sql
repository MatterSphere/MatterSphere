CREATE TABLE _was_is 				
( 				
	wiCode NVARCHAR ( 15 ) 			
	, wiWas NVARCHAR ( 15 ) 			
	, wiIs NVARCHAR ( 15 ) 			
)				
INSERT INTO _was_is 				
( 				
	WICODE			
	, WIWAS			
	, WIIS			
)				
SELECT 				
	'COUNTRY'			
	, CTRYCODE			
	, NULL			
FROM				
	DBCOUNTRY			
LEFT JOIN				
	_WAS_IS ON _WAS_IS.WIWAS = DBCOUNTRY.CTRYCODE AND _WAS_IS.WICODE = 'COUNTRY'			
WHERE				
	_WAS_IS.WIWAS IS NULL			
				
				
-- 2) COMPARE COUNTRY DESCRIPTIONS				
select				
	_WAS_IS.*			
	, TE_3E_SUPPORT.DBO.COUNTRY.Description					
from 				
	_was_is			
INNER JOIN 				
	TE_3E_SUPPORT.DBO.COUNTRY on TE_3E_SUPPORT.DBO.COUNTRY.CODE = _WAS_IS.WIIS AND _WAS_IS.WICODE = 'COUNTRY'			
WHERE				
	TE_3E_SUPPORT.DBO.COUNTRY.Description != _was_is.wiWas 			
				
				
